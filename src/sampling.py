
import numpy as np
from torchvision import datasets, transforms


def mnist_iid(dataset, num_users):
    """
    Sample I.I.D. client data from MNIST dataset
    :param dataset:
    :param num_users:
    :return: dict of image index
    """
    num_items = int(len(dataset)/num_users)
    dict_users, all_idxs = {}, [i for i in range(len(dataset))]
    for i in range(num_users):
        dict_users[i] = set(np.random.choice(all_idxs, num_items,
                                             replace=False))
        all_idxs = list(set(all_idxs) - dict_users[i])
    return dict_users


def mnist_noniid(dataset, num_users):
    """
    Sample non-I.I.D client data from MNIST dataset
    :param dataset:
    :param num_users:
    :return:
    """
    # 60,000 training imgs -->  200 imgs/shard X 300 shards
    num_shards, num_imgs = 200, 300
    idx_shard = [i for i in range(num_shards)]
    dict_users = {i: np.array([]) for i in range(num_users)}
    idxs = np.arange(num_shards*num_imgs)
    labels = dataset.train_labels.numpy()

    # sort labels
    idxs_labels = np.vstack((idxs, labels))
    idxs_labels = idxs_labels[:, idxs_labels[1, :].argsort()]
    idxs = idxs_labels[0, :]

    # divide and assign 2 shards/client
    for i in range(num_users):
        rand_set = set(np.random.choice(idx_shard, 2, replace=False))
        idx_shard = list(set(idx_shard) - rand_set)
        for rand in rand_set:
            dict_users[i] = np.concatenate(
                (dict_users[i], idxs[rand*num_imgs:(rand+1)*num_imgs]), axis=0)
    return dict_users


def mnist_noniid_unequal(dataset, num_users):
    """
    Sample non-I.I.D client data from MNIST dataset s.t clients
    have unequal amount of data
    :param dataset:
    :param num_users:
    :returns a dict of clients with each clients assigned certain
    number of training imgs
    """
    # 60,000 training imgs --> 50 imgs/shard X 1200 shards 数据集分片
    num_shards, num_imgs = 1200, 50
    idx_shard = [i for i in range(num_shards)]
    dict_users = {i: np.array([]) for i in range(num_users)}  # 按照用户数量分配空数组 用于储存训练数据
    idxs = np.arange(num_shards * num_imgs)  # 分配每个数据的下标
    labels = dataset.train_labels.numpy()  # 数据集数据的标签

    # sort labels 标签排序
    idxs_labels = np.vstack((idxs, labels))  # 合并两个List
    idxs_labels = idxs_labels[:, idxs_labels[1, :].argsort()]  # 输出标签大小排序的下标
    idxs = idxs_labels[0, :] # 取第一行

    # Minimum and maximum shards assigned per client 定义一个学习节点的最大和最小分片数
    min_shard = 1
    max_shard = 30

    # Divide the shards into random chunks for every client 拆分数据集
    # s.t the sum of these chunks = num_shards
    random_shard_size = np.random.randint(min_shard, max_shard+1,
                                          size=num_users)  # 随机生成分片数量
    random_shard_size = np.around(random_shard_size /
                                  sum(random_shard_size) * num_shards)  # 按比例分配
    random_shard_size = random_shard_size.astype(int)  # 类型转换

    # Assign the shards randomly to each client
    # 如果生成的随机分片list总数大于规定的分片总数
    if sum(random_shard_size) > num_shards:

        for i in range(num_users):
            # First assign each client 1 shard to ensure every client has at least one shard of data
            rand_set = set(np.random.choice(idx_shard, 1, replace=False))  # 从idx_shard中随机选择一个下标
            idx_shard = list(set(idx_shard) - rand_set) # 从下标的list中去掉已经被分配掉的分片
            for rand in rand_set:
                dict_users[i] = np.concatenate(
                    (dict_users[i], idxs[rand*num_imgs:(rand+1)*num_imgs]),axis=0)
                # 把idxs中随机到的分片贴到对应的dict_user中

        random_shard_size = random_shard_size-1

        # Next, randomly assign the remaining shards 将剩下的分片随机分配
        for i in range(num_users):
            if len(idx_shard) == 0:
                continue
            shard_size = random_shard_size[i]
            if shard_size > len(idx_shard):
                shard_size = len(idx_shard)
            rand_set = set(np.random.choice(idx_shard, shard_size,
                                            replace=False))
            idx_shard = list(set(idx_shard) - rand_set)
            for rand in rand_set:
                dict_users[i] = np.concatenate(
                    (dict_users[i], idxs[rand*num_imgs:(rand+1)*num_imgs]),
                    axis=0)
    else:

        for i in range(num_users):
            shard_size = random_shard_size[i]  # 当前user的分片数量
            rand_set = set(np.random.choice(idx_shard, shard_size,
                                            replace=False))  # 从全集中随机选取对应数量的分片
            idx_shard = list(set(idx_shard) - rand_set)  # 从全集中去掉已经被选择的分片
            # 在对应的用户字典中加入分配的分片
            for rand in rand_set:
                dict_users[i] = np.concatenate(
                    (dict_users[i], idxs[rand*num_imgs:(rand+1)*num_imgs]),
                    axis=0)

        if len(idx_shard) > 0:
            # Add the leftover shards to the client with minimum images
            shard_size = len(idx_shard)
            # Add the remaining shard to the client with lowest data
            k = min(dict_users, key=lambda x: len(dict_users.get(x)))
            rand_set = set(np.random.choice(idx_shard, shard_size,
                                            replace=False))
            idx_shard = list(set(idx_shard) - rand_set)
            for rand in rand_set:
                dict_users[k] = np.concatenate(
                    (dict_users[k], idxs[rand*num_imgs:(rand+1)*num_imgs]),
                    axis=0)

    return dict_users


